package oa;

public class EvenOddCompare {
    public static void main(String[] args) {
        int a = 0;
        int b = a;
        b = 10;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        String s1 = "hi";
        String s2 = s1;
        s2 = "ohd";
        System.out.println("s1 = " + s1);
        System.out.println("s2 = " + s2);
    }
}
